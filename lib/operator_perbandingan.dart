void main() {
  print(10 == 10); //persamaan
  print(10 != 10); //tidak sama
  print(10 > 10); //lebih besar
  print(10 < 10); //lebih kecil
  print(10 >= 10); //lebih besar sama dengan
  print(10 <= 10); // lebih kecil sama dengan
  print('eko' == 'eko'); //persamaan
  print('eko' != 'eko'); //tidak sama
}