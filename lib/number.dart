/**untuk di Flutter tipe data number hanya ada 2
 * Yaitu:
 * - INT
 * - DOUBLE
 * bisa dideklarasikan ulang
*/

void main() {
  int number1 = 10; //hanya bilangan bulat
  double number2 = 10.5; //bisa desimal

  print(number1);
  print(number2);

  //NUM
  //tipe data yang bisa INT dan DOUBLE
  num number3 = 10;//menggunakan INT atau bilangan BULAT
  print(number3);

  number3 = 10.5;//menggunakan DOUBLE atau bilangan DESIMAL
  print(number3);


}