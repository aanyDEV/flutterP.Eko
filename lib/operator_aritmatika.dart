import 'dart:ui';

void main() {
  var first = -5;
  var second = -3;

  print(first + second); //penambahan
  print(first - second); //pengurangan
  print(first * second); //perkalian
  print(first / second); //pembagian hasil double
  print(first ~/ second); //pembagian hasil int
  print(first % second); //modulus atau sisa bagi
}